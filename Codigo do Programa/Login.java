    private void stopCamera(){
         myThread.runnable = false;
        webSource.release();
    }

    private void startCamera(){
         webSource = new VideoCapture(0);
            myThread = new Login.DaemonThread();
            Thread t = new Thread(myThread);
            t.setDaemon(true);
            myThread.runnable = true;
            t.start();
    }
